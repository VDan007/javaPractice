public class Wrapper__classes {
    public static void main(String[] args){


        // wrapper class = provides a way to use primitive data types as reference dastatypes
        //                 reference data types contain usefull methods
        //                 can be used with collections (ex.ArrayList)



        // primitive    wrapper

        // boolean      Boolean
        // char         Character
        // int          Integer    
        // double       Double

        // autoboxing 
        // unboxing


        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.1456;
        String e = "Bro";

        

        System.out.println(a.toString());
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        

    }

}
